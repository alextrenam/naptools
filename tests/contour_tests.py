contour_data.print_data("0002")
# ============================================================================
#
# Custom plotting parameters
#
# ============================================================================
plotting_params = {}
# ============================================================================
#
# Outputs
#
# ============================================================================
contour_plot = nap.ContourPlot(contour_data)
contour_plot.plot(
    "u", "0002", "./results/u_contour_0002.pdf", parameters=plotting_params
)
